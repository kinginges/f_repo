#include <iostream>
#include <cmath>

int main()
{
    std::cout << pow(2, 3) << std::endl; // number to a power
    std::cout << abs(-23) << std::endl;  // absolute of a number
    std::cout << sin(10) << std::endl;
    std::cout << sqrt(6) << std::endl;
    std::cout << ceil(2.37) << std::endl;  // round to nearest highest whole number
    std::cout << floor(2.67) << std::endl; // round to nearest lowest
    std::cout << exp(2) << std::endl;
    std::cout << log(4) << std::endl;
    std::cout << log10(200) << std::endl;

    std::cout << "Your code ran successfully! You did great!";
    return 0;
};